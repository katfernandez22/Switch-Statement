let restaurantItem = "Chicken";
let price;

switch (restaurantItem) {
  case "Burger":
    price = "$10";
    break;
  case "Steak":
    price = "$25";
    break;
  case "Salmon":
    price = "$20";
    break;
  case "Shrimp":
    price = "$20";
    break;
  case "Chicken":
    price = "$15";
    break;
  default:
    price = "not available";
}